package com.algorithm.zxie.tree.adt;

public interface BinaryTree {

	/**
	 * count the binary tree height
	 * @param root tree root node
	 * @return tree height
	 */
	public int height();

	/**
	 * count the number of tree nodes
	 * @param root tree root node
	 * @return tree node number
	 */
	public int count();

}
